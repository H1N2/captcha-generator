    protected $widget     = [];
    protected $max_widget = 0;

    protected $fonts = [];
    protected $files = null;

    protected function configure()
    {
        $this->setName('command:captcha-format-font')//这里的command:name即是调用名称,如改成make:controller,是调用方式将变为php app make:controller
        ->setDescription('格式化字体文件名，删除无效字体');
    }

    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $this->files = new Filesystem();

        $this->fonts = $this->files->files(__DIR__ . '/../Assets/fonts');

        $tmp_arr = [];
        foreach ($this->fonts as $font) {
            if (preg_match('/font_\d+/', $font->getFilename(), $matches)) {
                $tmp_arr[ $matches[0] ] = $font;
            }
        }
        $this->fonts = $tmp_arr;
        unset($tmp_arr);
//        return;

        dump('准备数据中……');
        $fonts = $this->getFonts();

        foreach ($fonts as $reset_font) {
            preg_match('/font_\d+/', $reset_font, $matches);
            if (!$matches) {
                dump("--- not allow the {$reset_font} font name");
                continue;
            }
            $font_name = $matches[0];

            if (!array_key_exists($font_name, $this->fonts)) {
                dump("the {$font_name} font file not exist");
                continue;
            }

            $font = $this->fonts[ $font_name ];


            //判断无效
            if ($reset_font[0] == '-' || preg_match('/^font_\d+$/', $reset_font)) {
                if (unlink($font->getRealPath())) {
                    dump("success deleted font file: {$font_name}");
                } else {
                    dump("failed delete font file: {$font_name}");
                }
                continue;
            }

            //改名
            if (preg_match('/(^\_(\_)?font_\d+$)|(^\+\_(\_)?font_\d+$)|(^\+font_\d+$)/', $reset_font)) {
                $font_pathname = $font->getRealPath();
                $new_filepath  = dirname($font_pathname) . '/' . $reset_font . '.ttf';

                $status = rename($font_pathname, $new_filepath);

                if ($status) {
                    dump("success reset font name: {$font_name}");
                } else {
                    dump("failed reset font name: {$font_name}");
                }

                continue;
            }
        }



    }


